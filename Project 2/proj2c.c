#include <stdio.h>

int main()
{
    int i;
    int A[21];
    int *B[3];

    A[0] = 0;
    A[1] = 1;
    A[2] = 1;
    for (i = 3 ; i < 21 ; i++)
        A[i] = A[i-3]+A[i-2]+A[i-1];

    for (i = 0 ; i < 21 ; i++)
        printf("%d: %d\n", i, A[i]);

    B[0] = A;
    B[1] = A+7;
    B[2] = A+14;

    char C[14][20] = {"A[2]:", "*(A+5)-*(A+3):", "(A+7)-(A+2):", "A[5]-*(A+4)", "*A:", 
    "(A+12)-B[1]:", "B[1][4]:", "*(A+4):", "B[2][3]-A[16]:", "&(B[2][3])-&(A[15]):", 
    "B[2][0]-B[1][6]:", "*(&A[6]):", "B[2][0]-B[1][7]:"};
    int D[14] = {A[2], *(A+5)-*(A+3), (A+7)-(A+2), A[5]-*(A+4), *A, (A+12)-B[1], B[1][4], *(A+4), B[2][3]-A[16], &(B[2][3])-&(A[15]), B[2][0]-B[1][6], *(&A[6]), 
    B[2][0]-B[1][7]};

    int k;
    for (k=0; k<13; k++)
    {
        if ((k+1)%2 == 0)
        {
            printf("%s ", C[k]);
            printf("%d\n", D[k]);
        }
        else
        {
            printf("%s ", C[k]);
            printf("%d\t\t\t\t\t", D[k]);
        }
    }
    printf("\n");
}
